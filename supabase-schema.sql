-- Mevcut tarayıcı arayüzünün ortak ve sürüm kontrollü uygulama durumu.
-- JSONB yapısı ilk geçişte ekranları yeniden yazmadan çok kullanıcılı çalışmayı sağlar.
create table if not exists public.app_state (
  id text primary key check (id = 'main'),
  payload jsonb not null default '{}'::jsonb,
  version bigint not null default 0,
  updated_by uuid references public.profiles(id),
  updated_at timestamptz not null default now()
);

insert into public.app_state (id, payload, version)
values ('main', '{}'::jsonb, 0)
on conflict (id) do nothing;

alter table public.app_state enable row level security;

drop policy if exists app_state_read on public.app_state;
create policy app_state_read on public.app_state for select to authenticated
using (true);

-- Kayıtlar doğrudan güncellenmez; sürüm kontrolü yapan RPC kullanılır.
create or replace function public.save_app_state(
  expected_version bigint,
  new_payload jsonb
)
returns table(version bigint, updated_at timestamptz)
language plpgsql
security definer
set search_path = public
as $$
declare
  current_version bigint;
  saved_at timestamptz;
begin
  if public.current_app_role() not in ('Yönetici', 'Kullanıcı') then
    raise exception 'POLPRO_WRITE_FORBIDDEN' using errcode = '42501';
  end if;

  if new_payload is null or jsonb_typeof(new_payload) <> 'object' then
    raise exception 'POLPRO_INVALID_PAYLOAD' using errcode = '22023';
  end if;

  select state.version
    into current_version
    from public.app_state state
   where state.id = 'main'
   for update;

  if current_version is distinct from expected_version then
    raise exception 'POLPRO_VERSION_CONFLICT expected %, current %', expected_version, current_version
      using errcode = '40001';
  end if;

  update public.app_state
     set payload = new_payload,
         version = current_version + 1,
         updated_by = auth.uid(),
         updated_at = now()
   where id = 'main'
   returning app_state.version, app_state.updated_at
        into version, saved_at;

  updated_at := saved_at;
  return next;
end;
$$;

revoke all on function public.save_app_state(bigint, jsonb) from public;
grant execute on function public.save_app_state(bigint, jsonb) to authenticated;

alter table public.app_state replica identity full;
do $$
begin
  if not exists (
    select 1
      from pg_publication_tables
     where pubname = 'supabase_realtime'
       and schemaname = 'public'
       and tablename = 'app_state'
  ) then
    alter publication supabase_realtime add table public.app_state;
  end if;
end $$;
