do $$
declare table_name text;
begin
  foreach table_name in array array['projects','project_managers','tasks','costs','suppliers','project_files','activities','procurements','manufacturings','risks','issues','changes','actions','document_revisions','quality_records','project_closures','capacity_records']
  loop
    execute format('drop policy if exists %I_read on public.%I', table_name, table_name);
    execute format('create policy %I_read on public.%I for select to authenticated using (true)', table_name, table_name);
    execute format('drop policy if exists %I_insert on public.%I', table_name, table_name);
    execute format('create policy %I_insert on public.%I for insert to authenticated with check (public.current_app_role() in (''Yönetici'',''Kullanıcı''))', table_name, table_name);
    execute format('drop policy if exists %I_update on public.%I', table_name, table_name);
    execute format('create policy %I_update on public.%I for update to authenticated using (public.current_app_role() in (''Yönetici'',''Kullanıcı'')) with check (public.current_app_role() in (''Yönetici'',''Kullanıcı''))', table_name, table_name);
    execute format('drop policy if exists %I_delete on public.%I', table_name, table_name);
    execute format('create policy %I_delete on public.%I for delete to authenticated using (public.current_app_role() = ''Yönetici'')', table_name, table_name);
  end loop;
end $$;

